    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
try {
            // Lấy dữ liệu từ form
            String hoTen = txtHoTen.getText().trim();
            String diaChi = txtDiaChi.getText().trim();
            String matKhau = txtmatKhau.getText().trim();
            Boolean gioiTinh = rdoNam.isSelected();
            String email = txtEmail.getText().trim();
            String soDienThoai = txtSoDienThoai.getText().trim();
            String chucVu = txtChucVu.getText().trim();
            Date ngayTao = (jdaNgayTao.getDate() != null) ? new Date(jdaNgayTao.getDate().getTime()) : null;
            Date ngaySua = (jdaNgaySua.getDate() != null) ? new Date(jdaNgaySua.getDate().getTime()) : null;
            int trangThai = 1;

            // Kiểm tra rỗng
            if (hoTen.isEmpty() || diaChi.isEmpty() || matKhau.isEmpty()
                    || email.isEmpty() || soDienThoai.isEmpty() || chucVu.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Vui lòng nhập đầy đủ thông tin.");
                return;
            }

            // Kiểm tra định dạng
            if (!email.matches("^[\\w-\\.]+@([\\w-]+\\.)+[\\w-]{2,4}$")) {
                JOptionPane.showMessageDialog(this, "Email không đúng định dạng.");
                return;
            }

            if (!soDienThoai.matches("^0\\d{9}$")) {
                JOptionPane.showMessageDialog(this, "Số điện thoại phải đủ 10 số và bắt đầu bằng 0.");
                return;
            }

            // Tạo đối tượng nhân viên
            NhanVienResponse kh = new NhanVienResponse();
            kh.setTenNhanVien(hoTen);
            kh.setDiaChi(diaChi);
            kh.setMatKhau(matKhau);
            kh.setGioiTinh(gioiTinh);
            kh.setEmail(email);
            kh.setSoDienThoai(soDienThoai);
            kh.setChucVu(chucVu);
            kh.setNgayTao(ngayTao);
            kh.setNgaySua(ngaySua);
            kh.setTrangThai(trangThai);

            // Gọi repo để thêm (không cần set mã nếu mã tự tăng)
            nvrepo.addNhanVien(kh);

            // Load lại danh sách từ database và đổ lại bảng
            list_nv = nvrepo.getAll();
            fillTable(list_nv);

            JOptionPane.showMessageDialog(this, "Thêm nhân viên thành công!");

        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Lỗi khi thêm nhân viên.");
        }
    }//GEN-LAST:event_btnThemActionPerformed

