  private void fillTable(ArrayList<NhanVienResponse> list_nv) {
        dtmkh.setRowCount(0);
        int index = 1;
        for (NhanVienResponse nv : list_nv) {
            SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");
            dtmkh.addRow(new Object[]{
                index++,
                nv.getMaNhanVien(),
                nv.getTenNhanVien(),
                nv.getDiaChi(),
                nv.getMatKhau(),
                nv.isGioiTinh() ? "Nam" : "Nữ",
                nv.getEmail(),
                nv.getSoDienThoai(),
                nv.getChucVu(),
                nv.getNgayTao() != null ? sdf.format(nv.getNgayTao()) : "",
                nv.getNgaySua() != null ? sdf.format(nv.getNgaySua()) : "",
                nv.getTrangThai() == 1 ? "Hoạt động" : "không hoạt động"
            });
        }

    }

