    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        int selectedRow = tblKhachHang.getSelectedRow();

        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn sản phẩm cần sửa!", "Thông báo", JOptionPane.WARNING_MESSAGE);
            return;
        }

        if (!validateFormKhachHang()) {
            return;
        }

        KhachHangResponse kh = khrepo.getAll().get(selectedRow);

        // Lấy dữ liệu từ form
        String hoTen = txtHoTen.getText();
        String soDienThoai = txtSoDienThoai.getText();
        boolean gioiTinh = rdoNam.isSelected();
        String diaChi = txtDiaChi.getText();
        Date ngayTao = jdaNgayTao.getDate();

        kh.setTenKhachHang(hoTen);
        kh.setSoDienThoai(soDienThoai);
        kh.setGioiTinh(gioiTinh);
        kh.setDiaChi(diaChi);
        kh.setNgayTao(ngayTao);
        boolean success = khrepo.sua(kh.getId(), kh);

        if (success) {
            JOptionPane.showMessageDialog(this, "Cập nhật khách hàng thành công!");
            list_kh = khrepo.getAll();
            fillTable(list_kh); // Cập nhật lại bảng
        } else {
            JOptionPane.showMessageDialog(this, "Cập nhật thất bại!");
        }
    }//GEN-LAST:event_btnSuaActionPerformed

    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        try {
            if (!validateFormKhachHang()) {
                return;
            }

            String hoTen = txtHoTen.getText().trim();
            Boolean gioiTinh = rdoNam.isSelected();
            String soDienThoai = txtSoDienThoai.getText().trim();
            String diaChi = txtDiaChi.getText().trim();
            Date ngayTao = (jdaNgayTao.getDate() != null) ? new Date(jdaNgayTao.getDate().getTime()) : null;
            int trangThai = 1;

            KhachHangResponse kh = new KhachHangResponse();
            kh.setTenKhachHang(hoTen);
            kh.setSoDienThoai(soDienThoai);
            kh.setDiaChi(diaChi);
            kh.setGioiTinh(gioiTinh);
            kh.setNgayTao(ngayTao);
            kh.setTrangThai(trangThai);
            khrepo.addKhachHang(kh);

            list_kh = khrepo.getAll();
            fillTable(list_kh);
            JOptionPane.showMessageDialog(this, "Thêm khách hàng thành công!");
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnThemActionPerformed

