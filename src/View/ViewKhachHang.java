    private void fillTable(ArrayList<KhachHangResponse> list_kh) {
        dtmkh.setRowCount(0);
        index = 1;
        for (KhachHangResponse kh : list_kh) {
            SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");
            dtmkh.addRow(new Object[]{
                index++,
                kh.getMaKhachHang(),
                kh.getTenKhachHang(),
                kh.isGioiTinh() ? "Nam" : "Nữ",
                kh.getSoDienThoai(),
                kh.getDiaChi(),
                kh.getNgayTao() != null ? sdf.format(kh.getNgayTao()) : "",});
        }
    }

    private boolean validateFormKhachHang() {
        String hoTen = txtHoTen.getText().trim();
        String soDienThoai = txtSoDienThoai.getText().trim();
        String diaChi = txtDiaChi.getText().trim();
        Date ngayTao = jdaNgayTao.getDate();

        if (hoTen.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Họ tên không được để trống");
            return false;
        }

        if (!hoTen.matches("^[\\p{L} ]{3,}$")) {
            JOptionPane.showMessageDialog(this, "Họ tên phải có ít nhất 3 ký tự và chỉ chứa chữ cái");
            return false;
        }

        if (soDienThoai.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Số điện thoại không được để trống");
            return false;
        }

        if (!soDienThoai.matches("0\\d{9}")) {
            JOptionPane.showMessageDialog(this, "Số điện thoại phải bắt đầu bằng 0 và có 10 chữ số");
            return false;
        }

        if (diaChi.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Địa chỉ không được để trống");
            return false;
        }

        if (ngayTao == null) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn ngày tạo");
            return false;
        }

        return true;
    }

